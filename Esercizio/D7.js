function only_even(mixed_array) {
  let even_number = [];
  for (let i = 0; i < mixed_array.length; i++) {
    if (mixed_array[i] % 2 === 0) {
      even_number.push(mixed_array[i]);
    }
  }
  console.log(even_number);
}
